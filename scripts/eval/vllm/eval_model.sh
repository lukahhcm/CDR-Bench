#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RELEASE_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
cd "${RELEASE_ROOT}"

TRACKS="${TRACKS:-}"
MODEL="${MODEL:-local-model}"
MODEL_SLUG="${MODEL_SLUG:-${MODEL}}"
BACKEND="vllm"
BASE_URL="${BASE_URL:-http://127.0.0.1:8000/v1}"
API_KEY="${API_KEY:-EMPTY}"
EVAL_ROOT="${EVAL_ROOT:-${BENCHMARK_ROOT:-data/benchmark_v3}}"
BENCHMARK_TRACKS_SUBDIR="${BENCHMARK_TRACKS_SUBDIR:-tracks}"
EVALUATION_ROOT="${EVALUATION_ROOT:-${OUTPUT_ROOT:-data/evaluation}}"
PROMPT_VARIANT_INDICES="${PROMPT_VARIANT_INDICES:-all}"
PROMPT_STYLE_IDS="${PROMPT_STYLE_IDS:-}"
PROMPT_VARIANT_SAMPLE_SIZE="${PROMPT_VARIANT_SAMPLE_SIZE:-3}"
PROMPT_VARIANT_SAMPLING_SEED="${PROMPT_VARIANT_SAMPLING_SEED:-0}"
MAX_SAMPLES="${MAX_SAMPLES:-0}"
MAX_INPUT_CHARS="${MAX_INPUT_CHARS:-0}"
MAX_TOKENS="${MAX_TOKENS:-0}"
TEMPERATURE="${TEMPERATURE:-0}"
MAX_RETRIES="${MAX_RETRIES:-1}"
RETRY_SLEEP_SECONDS="${RETRY_SLEEP_SECONDS:-2.0}"
CONCURRENCY="${CONCURRENCY:-128}"
PROGRESS_EVERY="${PROGRESS_EVERY:-20}"
RESUME="${RESUME:-true}"
RESUME_ONLY_EXISTING_ROWS="${RESUME_ONLY_EXISTING_ROWS:-false}"
ENABLE_THINKING="${ENABLE_THINKING:-false}"

export TRACKS EVAL_ROOT BENCHMARK_TRACKS_SUBDIR MODEL MODEL_SLUG BACKEND BASE_URL API_KEY PROMPT_API_KEY EVALUATION_ROOT OUTPUT_ROOT PREDICTIONS_ROOT PROMPT_VARIANT_INDICES PROMPT_STYLE_IDS PROMPT_VARIANT_SAMPLE_SIZE PROMPT_VARIANT_SAMPLING_SEED MAX_SAMPLES MAX_INPUT_CHARS MAX_TOKENS TEMPERATURE MAX_RETRIES RETRY_SLEEP_SECONDS CONCURRENCY PROGRESS_EVERY RESUME RESUME_ONLY_EXISTING_ROWS ENABLE_THINKING

exec bash "${RELEASE_ROOT}/scripts/eval/run_model_eval.sh" "$@"
